def drawMaze():
    global playerSightDist
    global mazeArray, numMazeRow, numMazeCol, mazeSquareSpace, mazeSquares, mazeCoordX, mazeCoordY

    mazeSquareColours = [['black' for j in range(numMazeCol)] for i in range(numMazeRow)]
    closestXPos = float('inf')
    closestYPos = float('inf')
    closestCol = 0
    closestRow = 0

    mazeSquares = []
    for row in range(numMazeRow):
        for col in range(numMazeCol):
            if mazeArray[row][col] == 1:
                mazeSquareColour = '#083806'
            
            else:
                mazeSquareColour = '#382206'

            mazeSquareX = mazeCoordX[row][col]
            mazeSquareY = mazeCoordY[row][col]

            if abs(playerX - mazeSquareX) < closestXPos:
                closestXPos = abs(playerX - mazeSquareX)
                closestCol = col
            
            if abs(playerY - mazeSquareY) < closestYPos:
                closestYPos = abs(playerY - mazeSquareY)
                closestRow = row

            playerMazeSquareDist = sqrt((mazeSquareX - playerX)**2 + (mazeSquareY - playerY)**2)
            if playerMazeSquareDist <= playerSightDist:
                mazeSquareColours[row][col] = mazeSquareColour

    placeSquareY = 0
    for row in range(closestRow - playerSight//2, closestRow + playerSight//2 + 1):
        placeSquareX = 0
        for col in range(closestCol - playerSight//2, closestCol + playerSight//2 + 1):
            mazeSquare = screen.create_rectangle(placeSquareX, placeSquareY, placeSquareX + mazeSquareSpace, placeSquareY + mazeSquareSpace, fill = mazeSquareColours[row][col], outline = mazeSquareColours[row][col])
            mazeSquares.append(mazeSquare)
            placeSquareX = placeSquareX + mazeSquareSpace
        placeSquareY = placeSquareY + mazeSquareSpace

