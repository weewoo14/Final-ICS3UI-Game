def keyDownHandler(event):
    global playerXSpeed, playerYSpeed, playerConstSpeed, playerMoveCooldown
    global mazeArray, numMazeRow, numMazeCol

    if playerMoveCooldown >= 0:
        if event.keysym == "Left":
            playerXSpeed = -playerConstSpeed
        
        if event.keysym == "Right":
            playerXSpeed = playerConstSpeed
        
        if event.keysym == "Up":
            playerYSpeed = -playerConstSpeed
        playerMoveCooldown = 0
        
        if event.keysym == "Down":
            playerYSpeed = playerConstSpeed

def keyUpHandler(event):
    global playerXSpeed, playerYSpeed
    playerXSpeed = 0
    playerYSpeed = 0


