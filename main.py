def runGame():
    global playerMoveCooldown
    global mazeSquares
    screen.create_rectangle(785,0,800,15,fill='red')
    setInitialValues()

    while True:
        drawMaze()
        drawPlayer()

        playerMoveCooldown += 1
        screen.update()
        sleep(0.03)
        
        for i in range(len(mazeSquares)):
            screen.delete(mazeSquares[i])
        
        updatePlayerPosition()


root.after(0,runGame)

